#include <gtest/gtest.h>

#include "NNL/game_asset/visual/render.hpp"

using namespace nnl;

static const std::vector<u8> bin_distance{0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
                                          0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0xab, 0x00, 0x00, 0x00, 0xcd, 0xcc,
                                          0xcc, 0x3d, 0x6f, 0x12, 0x03, 0x3b, 0x00, 0x00, 0x96, 0x43, 0x00, 0x00, 0xc8,
                                          0x43, 0x65, 0x00, 0x00, 0x00, 0x87, 0x00, 0x00, 0x00, 0xa3, 0x00, 0x00, 0x00,
                                          0x40, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00};

TEST(RenderConfig, IsOfType) { ASSERT_TRUE(render::IsOfType(bin_distance)); }

TEST(RenderConfig, ImportExport) {
  {
    auto dis = render::Import(bin_distance);
    auto bin_distance_re = render::Export(dis);

    ASSERT_TRUE(bin_distance == bin_distance_re);
  }
}
