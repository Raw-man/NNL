#include "NNL/game_asset/visual/minimap.hpp"

#include <gtest/gtest.h>

using namespace nnl;

static const std::vector<u8> bin_minimap{
    0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x98, 0xa9, 0x45, 0x00, 0x40, 0x2d, 0x44, 0x0a, 0xd7, 0x23, 0x3d, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x86, 0x42, 0x00, 0x00, 0x8e, 0x42, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x24, 0x42, 0x00, 0x00, 0x58, 0x42, 0x00, 0x00, 0xb4, 0x42, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0xa4, 0x42, 0x00, 0x00, 0xb8, 0x41, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00};

TEST(Minimap, IsOfType) { ASSERT_TRUE(minimap::IsOfType(bin_minimap)); }

TEST(Minimap, ImportExport) {
  auto mm = minimap::Import(bin_minimap);
  auto bin_minimap_re = minimap::Export(mm);

  ASSERT_TRUE(bin_minimap == bin_minimap_re);
}

TEST(Minimap, Convert) {
  auto mm = minimap::Import(bin_minimap);
  auto spos = minimap::Convert(mm, 128);

  auto mm_re = minimap::Convert(spos, 128);

  ASSERT_FLOAT_EQ(mm.anchor_x, mm_re.anchor_x);
  ASSERT_FLOAT_EQ(mm.anchor_z, mm_re.anchor_z);
  ASSERT_FLOAT_EQ(mm.pixels_per_unit, mm_re.pixels_per_unit);
  ASSERT_EQ(mm.markers.size(), mm_re.markers.size());
  for (std::size_t i = 0; i < mm.markers.size(); i++) {
    auto& m = mm.markers.at(i);
    auto& m_re = mm_re.markers.at(i);
    ASSERT_FLOAT_EQ(m.x, m_re.x);
    ASSERT_FLOAT_EQ(m.y, m_re.y);
    ASSERT_FLOAT_EQ(m.z, m_re.z);
  }
}
